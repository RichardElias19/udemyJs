function divisaoEresto(dividendo, divisor) {
    const divisao = Math.floor(dividendo / divisor)
    console.log(`a divisao do  ${dividendo} e do  ${divisor} e ${divisao}`)
    console.log (`o resto da divisao entre ${dividendo} e ${divisor} = ${dividendo % divisor}`) // e o resto da divisao
}
divisaoEresto(7, 3) // divisao e resto da divisao